    private void deleteEquipment() {
        String equipmentId = txtEquipmentID.getText().trim();

        // Check if the equipment ID is not empty
        if (equipmentId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide the Equipment ID to delete.");
            return;
        }

        try {
            // Call the controller's delete method
            String response = equipmentController.deleteEquipment(equipmentId);
            JOptionPane.showMessageDialog(this, response);
            loadTable(); // Reload the table after delete
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error deleting equipment.");
            e.printStackTrace();
        }
    }

    private void loadTable() {
        String[] columns = {"Equipment ID", "Category ID", "Branch ID", "Brand", "Model", "Purchase Year", 
                            "Base Price", "Security Deposit", "Status", "Active"};
        DefaultTableModel dtm = new DefaultTableModel(columns, 0);
        jTable1.setModel(dtm);

        try {
            // Call the controller to get all equipment records
            ArrayList<EquipmentDto> equipmentList = equipmentController.getAllEquipment();
            for (EquipmentDto equipment : equipmentList) {
                // Add each equipment record as a row in the table
                dtm.addRow(new Object[] {
                    equipment.getEquipmentId(),
                    equipment.getCategoryId(),
                    equipment.getBranchId(),
                    equipment.getBrand(),
                    equipment.getModel(),
                    equipment.getPurchaseYear(),
                    equipment.getBasePricePerDay(),
                    equipment.getSecurityDeposit(),
                    equipment.getStatus(),
                    equipment.isIsActive() ? "Active" : "Inactive"
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading equipment data.");
            e.printStackTrace();
        }
    }
