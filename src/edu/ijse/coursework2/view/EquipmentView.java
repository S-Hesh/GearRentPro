    private void saveEquipment() {
        String equipmentId = txtEquipmentID.getText().trim();
        String categoryId = txtCategoryID.getText().trim();
        String branchId = txtBranchID.getText().trim();
        String brand = txtBrand.getText().trim();
        String model = txtModel.getText().trim();
        String purchaseYear = txtPurchaseYear.getText().trim();
        String basePrice = txtBasePrice.getText().trim();
        String securityDeposit = txtSecurityDeposite.getText().trim();
        String status = txtStatus.getText().trim();
        String active = txtActiveNot.getText().trim();

        // Check for empty fields and show a message
        if (equipmentId.isEmpty() || categoryId.isEmpty() || branchId.isEmpty() || brand.isEmpty() || 
            model.isEmpty() || purchaseYear.isEmpty() || basePrice.isEmpty() || securityDeposit.isEmpty() || 
            status.isEmpty() || active.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        // Convert text fields into appropriate data types
        int purchaseYearInt = Integer.parseInt(purchaseYear);
        double basePricePerDay = Double.parseDouble(basePrice);
        double securityDepositDouble = Double.parseDouble(securityDeposit);
        boolean isActive = active.equalsIgnoreCase("1");

        // Create a DTO object to pass to the controller
        EquipmentDto dto = new EquipmentDto(
                equipmentId, 
                categoryId, 
                branchId, 
                brand, 
                model, 
                purchaseYearInt, 
                basePricePerDay, 
                securityDepositDouble, 
                status, 
                isActive
        );

        try {
            // Call the controller's save method
            String response = equipmentController.saveEquipment(dto);
            JOptionPane.showMessageDialog(this, response);
            loadTable(); // Reload the table after save
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saving equipment.");
            e.printStackTrace();
        }
    }

